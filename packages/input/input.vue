<template>
  <div :class="[
      'input',
      {
        'have-prefix-icon': prefixIcon,
        'have-suffix-icon': suffixIcon
      }
    ]">
    <textarea v-if="type === 'textarea'"
              class="input__textarea-inner c-normal c-size-m"
              :value="inputValue"
              v-bind="$attrs"
              v-on="inputListeners" />

    <template v-else>
      <hIcon v-if="prefixIcon"
             class="input__icon"
             :name="prefixIcon" />

      <slot name="prepend"></slot>

      <input :class="[
          'input__input-inner',
          'c-normal',
          'c-size-m',
          {
            'have-prepand': havePrepand,
            'have-append': haveAppend
          }
        ]"
             :type="type"
             :value="inputValue"
             v-bind="$attrs"
             v-on="inputListeners" />

      <slot name="append"></slot>

      <hIcon v-if="suffixIcon"
             class="input__icon"
             :name="suffixIcon" />
    </template>
  </div>
</template>

<script>
import hIcon from "../icon/index.vue";
export default {
  name: "h-input",
  props: {
    type: {
      type: String,
      default: "text",
    },
    value: {
      type: [String, Number],
      default: "",
    },
    prefixIcon: {
      type: String,
    },
    suffixIcon: {
      type: String,
    },
  },
  components: {
    hIcon,
  },
  model: {
    prop: "value",
    event: "input",
  },
  data() {
    return {
      inputValue: "",
    };
  },
  computed: {
    havePrepand() {
      return this.$slots.prepend;
    },
    haveAppend() {
      return this.$slots.append;
    },
    inputListeners() {
      return Object.assign({}, this.$listeners, {
        input: (event) => this.$emit("input", event.target.value),
      });
    },
  },
  watch: {
    value: {
      handler(newVal) {
        this.inputValue = newVal;
      },
      immediate: true,
    },
  },
};
</script>

